package day05_Concatenation;

public class StudentInfo {
    public static void main(String[] args) {

        String student_name = "Derya";
        int age = 29;
        char gender = 'W';
        String schoolName = "Cydeo";
        String phoneNumber1 = "(123)-456-7891";
        String phoneNumber2 = "(123)-666-5555";
        boolean fullTime = true;
        double GPA = 3.5;

        System.out.println("Student Name = " + student_name);
        System.out.println("Age = " + age);
        System.out.println("Gender = " + gender);
        System.out.println("School Name = " + schoolName);
        System.out.println("Phone Number 1 = " + phoneNumber1);
        System.out.println("Phone Number 2 = " + phoneNumber2);
        System.out.println("GPA = " + GPA);
        System.out.println("Full Time = " + fullTime);


    }
}


