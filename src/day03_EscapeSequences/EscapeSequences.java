package day03_EscapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#"); // \n: strats a new line

        System.out.println("-----------------------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println("-----------------------------------------");

        System.out.println("\tJava is cool programming language"); // \t: paragraph (tab)

        System.out.println("-----------------------------------------");

        System.out.println("/ \\");// \\: ( singel back slash) in order to print one backward slash we need to give two backward slashes

        System.out.println("-----------------------------------------");

        System.out.println("My favorite TV show is \"Game Of Trone\""); // \": in order to print double quote, we need to use \"



    }
}
