package day01_JavaPractice;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello, How are you today? \n I am good"); //newline
        System.out.println("\tJava is fun"); //horizontal tab
        System.out.println("\\"); //backslash
        System.out.println("My favorite programming language is \"Java\""); // double quote

    }
}
